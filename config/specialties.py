def get_specialty_id(name: str) -> str | None:
    """Return the section_medicine ID for a specialty name (exact or partial match)."""
    if name in SPECIALTY_IDS:
        return SPECIALTY_IDS[name]
    # Try case-insensitive partial match
    name_lower = name.lower()
    for key, val in SPECIALTY_IDS.items():
        if name_lower in key.lower():
            return val
    return None
